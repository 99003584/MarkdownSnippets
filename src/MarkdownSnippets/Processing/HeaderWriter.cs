using System.IO;

static class HeaderWriter
{
    public static void WriteHeader(string sourceFile, string rootDirectory, TextWriter writer)
    {
        var relativeSource = sourceFile
            .Substring(rootDirectory.Length)
            .Replace('\\', '/');
        writer.WriteLine("<!--");
        writer.WriteLine("GENERATED FILE - DO NOT EDIT");
        writer.WriteLine("This file was generated by [MarkdownSnippets](https://github.com/SimonCropp/MarkdownSnippets).");
        writer.WriteLine($"Source File: {relativeSource}");
        writer.WriteLine("To change this file edit the source file and then run MarkdownSnippets.");
        writer.WriteLine("-->");
    }
}